integer_var = 42          # int
float_var = 3.14          # float
bool_var = True           # bool
str_var = "Hello, World!" # str
none_var = None           # NoneType

print(type(integer_var))  # int
print(type(float_var))    # float
print(type(bool_var))     # bool
print(type(str_var))      # str
print(type(none_var))     # NoneType